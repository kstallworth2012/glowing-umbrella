function rootReducer(state=INITAL_STATE,action){

  switch(action.type){
  case 'TASK_COMPLETED':
  	return state.filter((t)=>{return t.isCompleted === true })
  case 'BUDGET_UNDER_THREE_HUNDRED_K':
  	return state.filter((t)=>{return t.budget < 300000})
  case 'ADD_TASK':
  	return [...state, {...action.task}]
  default:
  	return state 

  }
}

